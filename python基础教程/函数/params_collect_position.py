#-----------------------------------
# 传递参数 收集位置参数
#-----------------------------------

# 收集参数: 收集位置参数
# 语法: 一个 * 号
x = '唐僧'
y = '悟空'
z = ['八戒', '钉耙', '大耳朵']
w = '沙僧'

def westFour(param_x, *four):
    print(param_x)
    print(four)
# 顺序传入的参数会被收集到一个元组中
# 一个 * 号只收集位置参数, 不收集关键字参数
westFour(w, x, y, z)