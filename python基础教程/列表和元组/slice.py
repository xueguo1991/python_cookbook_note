arr = ['a', 'b', 'c', 'd', 'e', 'f']

# 切片会复制序列的部分或者全部
print(arr[:])
print(arr[3:4])

# 切片的起始值是该元素的索引， 结束值是切片剩余第一个元素的索引值
# 切片的第三个参数是步长
print(arr[0:5])
print(arr[0:5:2])

# 负数代表从序列的末尾开始数
print(arr[0:-5])

# 切片的结束值如果位于起始值之前， 返回空序列
print(arr[-5:-1])
print(arr[-1:-5])
print(arr[3:3])

# 切片的容错机制，对于不存在索引，只获取范围内存在的元素组成切片
print(arr[0:100])

# 切片的妙用：获取序列开头和结尾的元素
print(arr[:3])
print(arr[-3:])
