#-----------------------------------
# 字典方法 使用一个字典更新另一个字典
#-----------------------------------

x = {'师傅': '唐僧', '二徒弟': '八戒'}
x_add = {'行李': ['袈裟', '禅杖', '钵盂']}
# update不存在的元素时新增
print(x.update(x_add))
print(x)

x = {'师傅': '唐僧', '二徒弟': '八戒'}
x_add = {'师傅': '唐御弟'}
# update已存在的元素时替换该元素的值
x.update(x_add)
print(x)