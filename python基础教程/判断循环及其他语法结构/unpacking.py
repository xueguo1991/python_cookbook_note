# 序列解包操作
# ----------------------------------

# 元组序列解包
a, b, c = '唐僧', '悟空', '八戒'
print(a, b, c)
(a, b, c) = ('唐僧', '悟空', '八戒')
print(a, b, c)

# 字符串序列解包
a, b, c = 'god'
print(a, b, c)

# 列表序列解包
a, b, c = ['唐僧', '悟空', '八戒']
print(a, b, c)

# 使用 * 号收集多余的值
a, b, *c = ['唐僧', '悟空', '八戒', '沙僧', '行李', '白龙马']
print(a, b, c)

print()
# 注意事项
# ----------------------------------

