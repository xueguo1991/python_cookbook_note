# 解包变量数多, 报错
# a, b, c, d = ['唐僧', '悟空', '八戒']
# ValueError: not enough values to unpack (expected 4, got 3)

# 序列解包用途
# ----------------------------------

# 并行赋值
a, b = '唐僧', '孙悟空'
print(a, b)

# 对调变量值
a, b, = b, a
print(a, b)

# 丢弃不用的变量值
a, b, *_ = '唐僧', '八戒', '行李', '沙僧'
print()