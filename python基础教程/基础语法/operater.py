#-----------------------------------
# python 所有运算符
#-----------------------------------

# 加法运算符
print(100 + 125)
print(['a', 'b', 'c'] + ['d', 'e', 'f'])
print('abc' + 'def')

# 减法运算符
print(100 - 124)

# 乘法运算符
print(100 * 500)
print(['a', 'b', 'c'] * 3)
print('abc' * 3)

# 除法运算符
print(100 / 5)

# 取模运算符
print(104 % 5)

# 求商整数部分, 整数除
print(11 // 5)

# 幂运算符
print( 2 ** 8)

# 大于算符
print(3 > 2)

# 小于算符
print(3 < 2)

# 大于等于算符
print( 3 >= 2)

# 小于等于算符
print(3 <= 2)

# 逻辑且算符
print(3 > 2 & 3 < 2)
print(3 + 2 & 3 - 2)

# 逻辑或算符
print(0 | 5)

# in运算符
print('5' in 'abc')
print('I am' in 'I am god')